#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define MAX_SIZE 5 // Максимальний розмір масиву

// Функція для обробки масиву
void processArrayTask() {// Обробка масиву
    int a[MAX_SIZE];// Оголошення масиву
    int n, i;// Кількість елементів, лічильник циклу
    char buffer[100];// Буфер для вводу

    printf("Введіть кількість елементів масиву (до %d): ", MAX_SIZE);// Запит кількості елементів
    fgets(buffer, sizeof(buffer), stdin);// Зчитування рядка з вводу

    if (sscanf(buffer, "%d", &n) != 1 || n <= 0 || n > MAX_SIZE) {// Перевірка на коректність введення
        printf(" Некоректна кількість елементів! Спробуйте ще раз.\n");// Повідомлення про помилку
        return;
    }

    // Ввід масиву
    printf("Введіть %d цілих чисел:\n", n);// Запит на введення елементів масиву
    for (i = 0; i < n; i++) {// Цикл для введення елементів
        printf("a[%d] = ", i);// Запит на введення елемента
        fgets(buffer, sizeof(buffer), stdin);// Зчитування рядка з вводу

        if (sscanf(buffer, "%d", &a[i]) != 1) {//   Перевірка на коректність введення
            printf(" Некоректне число! Ввід скасовано.\n");// Повідомлення про помилку
            return;
        }
    }

    // Вивід парних елементів у стовпець
    printf("\nПарні елементи масиву:\n");
    for (i = 0; i < n; i++) {// Цикл для виводу парних елементів
        if (a[i] % 2 == 0) {// Перевірка на парність
            printf("%d\n", a[i]);// Вивід парного елемента
        }
    }

    // Вивід непарних елементів, менших за 10
    printf("\nНепарні елементи менші за 10:\n");// Запит на вивід непарних елементів
    for (i = 0; i < n; i++) {// Цикл для виводу непарних елементів
        if (a[i] % 2 != 0 && a[i] < 10) {// Перевірка на непарність та меншість за 10
            printf("%d ", a[i]);// Вивід непарного елемента
        }
    }

    printf("\n");
}

int main() {// Головна функція програми
    char input[100];// Буфер для вводу
    int choice;// Вибір користувача

    while (1) {// Основний цикл програми
        printf("\n-- Меню --\n");
        printf("1. Обробка масиву (завдання)\n");
        printf("0. Вийти\n");
        printf("Ваш вибір: ");
        fgets(input, sizeof(input), stdin);// Зчитування рядка з вводу

        if (sscanf(input, "%d", &choice) != 1) {// Перевірка на коректність введення
            printf(" Ви ввели не число! Спробуйте ще раз.\n");// Повідомлення про помилку
            continue;// Продовження циклу, якщо введення некоректне
        }

        if (choice == 1) {// Перевірка на вибір обробки масиву
            processArrayTask();// Виклик функції обробки масиву
        } else if (choice == 0) {// Перевірка на вихід з програми
            printf(" Вихід з програми...\n");
            break;// Вихід з циклу
        } else {// Перевірка на некоректний вибір
            printf(" Невірний вибір. Введіть 1 або 0.\n");// Повідомлення про помилку
        }
    }

    return 0;
}
