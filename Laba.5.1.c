#include <stdio.h>

#define MAX_SIZE 5 // Максимальний розмір масиву

// Функція для виконання завдання з масивом
void processArrayTask() {
    int a[MAX_SIZE]; // Масив для зберігання елементів
    int n, i;

    printf("Введіть кількість елементів масиву (до %d): ", MAX_SIZE);
    scanf("%d", &n);

    if (n <= 0 || n > MAX_SIZE) { // Перевірка на допустимий розмір масиву
        printf("Некоректна кількість елементів.\n");// Повідомлення про помилку
        return;
    }

    // Ввід масиву
    printf("Введіть %d цілих чисел:\n", n);
    for (i = 0; i < n; i++) {// Цикл для вводу елементів масиву
        scanf("%d", &a[i]);// Ввід елемента масиву
    }

    // Вивід парних елементів у стовпець
    printf("\nПарні елементи масиву:\n");// Вивід заголовка
    for (i = 0; i < n; i++) {// Цикл для проходження по масиву
        if (a[i] % 2 == 0) {// Якщо елемент парний
            printf("%d\n", a[i]);// Вивід елемента в стовпець
        }
    }

    // Вивід непарних елементів, які менші за 10
    printf("\nНепарні елементи менші за 10:\n");
    for (i = 0; i < n; i++) {// Цикл для проходження по масиву
        if (a[i] % 2 != 0 && a[i] < 10) {// Якщо елемент непарний і менший за 10
            printf("%d ", a[i]);// Вивід елемента в рядок
        }
    }

    printf("\n");
}

int main() {
    int choice;// Змінна для вибору користувача

    while (1) { // Нереально класна менюшка :)
        printf("\n-- Меню --\n");
        printf("1. Обробка масиву (завдання)\n");
        printf("0. Вийти\n");
        printf("Ваш вибір: ");
        scanf("%d", &choice);// Ввід вибору користувача

        if (choice == 1) {// Якщо користувач вибрав обробку масиву
            processArrayTask();// Виклик функції для обробки масиву
        } else if (choice == 0) {// Якщо користувач вибрав вихід
            printf("Вихід з програми...\n");
            break;// Завершення програми
        } else {// Якщо вибір некоректний
            printf("Невірний вибір. Спробуйте ще раз.\n");
        }
    }

    return 0;
}
